/* Resolve a Navigator-app location string like "Ocean Bar, Deck 3" to a ship-page deep link.
   Returns { url, deck, id, label } or null if not found. */
window.shipVenueLink = function(loc){
  if(!loc) return null;
  var key = String(loc).toLowerCase().replace(/\s+/g, ' ').trim();
  var hit = window.SHIP_VENUE_CATALOG[key];
  if(!hit){
    /* Try fuzzy: strip leading "the ", trailing extras */
    var simpler = key.replace(/^the\s+/, '');
    hit = window.SHIP_VENUE_CATALOG[simpler];
  }
  if(!hit) return null;
  return {
    url: 'ship.html?view=floor&deck=' + hit.deck + '&highlight=' + hit.id,
    deck: hit.deck,
    id: hit.id,
    label: loc
  };
};
